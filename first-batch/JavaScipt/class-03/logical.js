// Logical Operator

// &&
// true and true = true
// false and false = false
// false and true = false;
// true and false = false;
var num1 = 23;
var num2 = 56;
var num3 = 100;

var result = num1 < num2 && num2 > num3;
console.log(result);


// ||

// true and true = true
// false and false = false
// false and true = true;
// true and false = true;

var num1 = 23;
var num2 = 56;
var num3 = 100;

var result = num1 > num2 || num2 < num3;
console.log(result);


// !

// console.log("Result", !(4 > 5));
console.log("Result", !(64 < 22));
